<template>
	<div id="id">
		<div class="todo-container">
			<div class="todo-wrap">
				<MyHeader :addTodo="addTodo"></MyHeader>
				<MyList :todos="todos" :checkTodo="checkTodo" :delTodo="delTodo"></MyList>
				<MyFooter :todos="todos"></MyFooter>
			</div>
		</div>
	</div>
</template>

<script>
import MyHeader from '@/components/MyHeader.vue'
import MyFooter from '@/components/MyFooter.vue'
import MyList from '@/components/MyList.vue'
export default {
	name: 'App',
	components: {

		MyHeader, MyFooter, MyList
	},
	data() {
		return {
			// 数据在哪，操作数据的方法就在哪
			todos: [
				{ id: '001', title: 'eat', done: true },
				{ id: '002', title: 'drink', done: true },
				{ id: '003', title: 'play', done: true }
			],
		}
	},
	methods: {
		addTodo(todoObj) {
			this.todos.unshift(todoObj)
		},
		checkTodo(id) {
			this.todos.forEach((todo) => {
				if (todo.id === id)
					todo.done = !todo.done
			})
		},
		delTodo(id){
			this.todos = this.todos.filter((todo)=>{
				return todo.id != id
			})
		}
	}

}
</script>

